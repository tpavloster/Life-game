void printOutResult (int item) {
    for (int i=0; i<20; i++) {
        for (int k=0; k<20; k++){
            if (0 == gameArray[item][i][k]) {
                std::cout <<  ". ";
            } else {
                std::cout <<  "* ";
            }
        }
        std::cout << std::endl;
    }
}

int calculateNeighboursCount (int x, int y) {
    return gameArray[0][x-1][y] + gameArray[0][x-1][y+1] + gameArray[0][x][y+1] + gameArray[0][x+1][y+1] + gameArray[0][x+1][y] + gameArray[0][x+1][y-1] + gameArray[0][x][y-1] + gameArray[0][x-1][y-1];
}

void analyzeNextIteration () {
    for (int i=1; i<19; i++) {
        for (int k=1; k<19; k++){
            if (1==gameArray[0][i][k]) {
                if (3 < calculateNeighboursCount(i, k) || 2 > calculateNeighboursCount(i, k)){
                    gameArray[1][i][k] = 0;
                } else {
                    gameArray[1][i][k] = 1;
                }
            } else {
                if (3 == calculateNeighboursCount(i, k)) {
                    gameArray[1][i][k] = 1;
                } else {
                    gameArray[1][i][k] = 0;
                }
            }
        }
    }
}

void moveIterationResults () {
    for (int i=0; i<20; i++) {
        for (int k=0; k<20; k++){
            gameArray[0][i][k] = gameArray[1][i][k];
        }
    }
}

